/// Generate a 7-emoji SAS string from the shared secret and session ID.
pub fn generate(shared_secret: &[u8; 32], session_id: &[u8; 32]) -> Vec<String> {
    let hk = Hkdf::<Sha256>::new(Some(session_id), shared_secret);
    let mut okm = [0u8; 7];
    hk.expand(b"blindwire-sas-v1", &mut okm)
        .expect("HKDF expansion should never fail for 7 bytes");

    okm.iter()
        .map(|&idx| SAS_EMOJIS[idx as usize].to_string())
        .collect()
}

#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn test_sas_determinism() {
        let secret = [0x55u8; 32];
        let sid = [0xAAu8; 32];
        let sas1 = generate(&secret, &sid);
        let sas2 = generate(&secret, &sid);
        assert_eq!(sas1, sas2);
    }

    #[test]
    fn test_sas_role_symmetry() {
        let secret = [0x12u8; 32];
        let sid = [0x34u8; 32];
        // Both initiator and responder should see same SAS
        let sas = generate(&secret, &sid);
        assert_eq!(sas.len(), 7);
    }
}
